1.) Overall, the game seems a bit too easy in that the balls mostly will end up in their appropriate goals on their own.
2.) One way to make it even easier, would be to slow down the maximum speed on the colored balls. One way to make it more difficult, would be to add various barriers/obstacles 
    that are in the way and must be hit around.
3.) At first glance, this game slighlty reminded me of those old wooden "Labyrinth" puzzles where you have two knobs that tilted the entire playspace on the X and Z axes in order to guide
    a metal ball through the maze and avoid the hole traps. I wouldn't entirely be sure as to how to implement tilting the arena around like that, especially since the balls are all locked
    into the Y position and gravity isn't a factor, but I think having a mechanic like that would give it a wow factor. That, and adding in some celebratory effects and feedback such as a
    pop cheer audio effect and maybe some confetti bursting out of the goal that is scored on when the ball triggers it.
4.) The rapidly moving chaos balls are fun in my opinion. Also, having each ball have its own goal is fun. To me, trying to catch up with the colored balls is tedious due to the 
    overall mobility/lack thereof of the bumper. Additionally, something about the way that the balls interact with eachother and the environment feels off to me. Some of the randomness
    is fine, but the way everything kind of just flails about in a jerky manner is offputting and "tedious" on the eyes (although this could be an FPS issue, but my screen is 240hz).


